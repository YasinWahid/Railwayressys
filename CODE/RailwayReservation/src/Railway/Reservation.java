    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         String pno = txtpno.getText();
         String source = txtsource.getSelectedItem().toString();
         String destination = txtdestination.getSelectedItem().toString();
         String tno = txttno.getText();
         String tname = txttname.getText();
         String fare = txtfare.getText();
         SimpleDateFormat date_form = new SimpleDateFormat("yyyy-MM-dd");
         String date = date_form.format(txtdate.getDate());
         String ticket = txtticket.getText();
         String total = txttotal.getText();
        
        try {
            pst = con.prepareStatement("insert into  reservation(pno,source,destination,tno,tname,fare,date,ticket,total)values(?,?,?,?,?,?,?,?,?)");
        pst.setString(1, pno);
        pst.setString(2, source);
        pst.setString(3, destination);
        pst.setString(4, tno);
        pst.setString(5, tname);
        pst.setString(6, fare);
        pst.setString(7, date);
        pst.setString(8, ticket);
        pst.setString(9, total);
        int k = pst.executeUpdate();
        
        if(k==1)
        {
            JOptionPane.showMessageDialog(this, "Reserved");
        }
        else{
            JOptionPane.showMessageDialog(this, "Reservation Failure");
        }
               
        } catch (SQLException ex) {
            Logger.getLogger(addtrain.class.getName()).log(Level.SEVERE, null, ex);
        }   
        
    }//GEN-LAST:event_jButton1ActionPerformed

