   public void load()
   {
       String source = txtsource.getSelectedItem().toString();
       String destination = txtdestination.getSelectedItem().toString();
       
       try
       {
           pst = con.prepareStatement("SELECT * from traindetails where source = ? and destination = ?");
           pst.setString(1, source);
           pst.setString(2, destination);
           rs = pst.executeQuery();
           
           if(rs.next() == true)
           {
               txttno.setText(rs.getString(1));
                txttname.setText(rs.getString(2));
                 txtfare.setText(rs.getString(5));
           }
           else
           {
               JOptionPane.showMessageDialog(this, "Record Not Found");
           }
       }
       catch( Exception e)
       {
           e.printStackTrace();
       }
   }
    
    
    
    
    
    
    
    
