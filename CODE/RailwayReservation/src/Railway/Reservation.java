               public void Loadsource()
                {
               try
               {
                 pst = con.prepareStatement("SELECT source FROM traindetails");
                 rs = pst.executeQuery();
                 txtsource.removeAllItems();
            
                  while(rs.next())
                 {
                   txtsource.addItem(rs.getString(1));
                  }
                  }
                  catch( Exception e )
                  {
                   e.printStackTrace();
                  }
                  }
    
